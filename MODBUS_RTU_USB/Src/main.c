/*!
  * @brief      IO Function for modbus.
 */
int8_t readFun( uint8_t *buff, uint16_t *count )
{

    int32_t index =_usbBuffers.pos_process;
    int32_t len =_usbBuffers.RxBuffersLength[index];
    int32_t i;

    for( i=0; i<len; i++)
        *buff++ = _usbBuffers.RxBuffers[index][i];

    *count = i;

    _usbBuffers.pos_process++;

    if(_usbBuffers.pos_process>=_MaxRxBuffersCount) //reach the last buffer, need to rewind to 0
        _usbBuffers.pos_process=0;

    HAL_GPIO_TogglePin(GPIOI, GPIO_PIN_1 );

    return 0;

}

/*!
  * @brief      IO Function for modbus.
 */
int8_t writeFun( uint8_t *i, int16_t j )
{
    CDC_Transmit_FS( i, j );
    return 0;
}

/*!
  * @brief      IO Function for modbus.
 */
int8_t frameFun( void  )
{

    if( _usbBuffers.pos_process !=_usbBuffers.pos_receive)
        return 1;

    return 0;

}

